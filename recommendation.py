def get_recommendation(

    current_price,

    weighted_fair_value

):

    ########################################################
    # UPSIDE
    ########################################################

    upside = (

        (

            weighted_fair_value

            -

            current_price

        )

        /

        current_price

    )

    ########################################################
    # SIGNAL
    ########################################################

    if upside >= 0.20:

        signal = "BUY"

        color = "green"

    elif upside >= 0.05:

        signal = "HOLD"

        color = "orange"

    else:

        signal = "SELL"

        color = "red"

    ########################################################
    # RETURN
    ########################################################

    return {

        "upside": upside,

        "signal": signal,

        "color": color

    }
    
    
